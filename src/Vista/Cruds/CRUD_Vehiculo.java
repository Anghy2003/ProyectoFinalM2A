    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        //buscamos a vehiculo de la base
        for (int i = 0; i < listaVehiculosBase.size(); i++) {
            
            if (txtPlaca.getText().equalsIgnoreCase(listaVehiculosBase.get(i).getPlaca_Vehiculo())) {
                Boolean valido = false;//creamos una bandera para validar datos
                if (valido = txtColor.getText().matches("^[a-zA-Z]+$")) {
                        if (valido = txtAñoFab.getText().matches("[0-9]{4}")) {
                            Vehiculo encontrado = new Vehiculo(txtPlaca.getText().toUpperCase(), null, null, null, null);//generamos una persona con la cedula para buscar
                            ObjectSet resultado = BaseFinal.get(encontrado);//buscamos en la base
                            if (!resultado.isEmpty()) {
                                resultado.forEach((obj) -> {
                                    BaseFinal.delete(obj);//eliminamos cada objeto encontrado
                                });
                                //una vez eliminado procede a guardar para  "Modificar"
                                guardarVehiculo(BaseFinal, txtPlaca.getText().toUpperCase(), txtModelo.getText().toUpperCase(), (String) cmbMarca.getSelectedItem(), txtColor.getText().toUpperCase(), txtAñoFab.getText().toUpperCase());
                                mostrarDatos(BaseFinal);
                            } else {
                                JOptionPane.showMessageDialog(null, "Vehiculo no encontrada en la BD");
                            }
                            
                        } else {
                            JOptionPane.showMessageDialog(this, "Ingrese un año válido");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Ingrese un color sin numeros");
                    }
                resetCampos();
                
            }
        }
        //reset campos
        resetCampos();
        //visualizar botones necesarios
        btnBuscar.setVisible(true);
        btnRegistrar.setVisible(true);
        btnModificar.setVisible(false);
        btnEliminar.setVisible(false);
        txtPlaca.setEnabled(true);
        mostrarDatos(BaseFinal);
        
    }//GEN-LAST:event_btnModificarActionPerformed

    public static List<Vehiculo> getListaVehiculosBase(ObjectContainer BaseFinal) {//metodo para obtener la lista de la base
        List<Vehiculo> listaVehiculosBase = new ArrayList<>();
        ObjectSet<Vehiculo> vehiculos = BaseFinal.query(Vehiculo.class);
        for (Vehiculo vehiculo : vehiculos) {
            listaVehiculosBase.add(vehiculo);
        }
        return listaVehiculosBase;
    }
    
