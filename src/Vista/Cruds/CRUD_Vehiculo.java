    public static void guardarVehiculo(ObjectContainer BaseFinal,String placa_Vehiculo, String modelo_Vehiculo, String marca_Vehiculo, String color_Vehiculo, String anioFabricacion_Vehiculo) {

        Vehiculo vehiculo1 = new Vehiculo(placa_Vehiculo,  modelo_Vehiculo,  marca_Vehiculo,  color_Vehiculo,  anioFabricacion_Vehiculo);

        if (verificarVehiculo(BaseFinal, placa_Vehiculo, null, null, null, null)== 0) {//mandamos a verificar con el codigo
            BaseFinal.set(vehiculo1);
            JOptionPane.showMessageDialog(null,"Vehiculo guardado");
            
        } else {
            JOptionPane.showMessageDialog(null, "Vehiculo ya existe en la BD");
        }
    }
    public static int verificarVehiculo(ObjectContainer BaseFinal,String placa_Vehiculo, String modelo_Vehiculo, String marca_Vehiculo, String color_Vehiculo, String anioFabricacion_Vehiculo) {
        // Crear un nuevo objeto  para usar como plantilla de búsqueda
        Vehiculo BuscarVehi = new Vehiculo(placa_Vehiculo, null, null, null, null);//se ingreesará el valor del codigo y el resto nulo
        // Obtener todos los objetos que coincidan con la plantilla de búsqueda
        ObjectSet resultado = BaseFinal.get(BuscarVehi);//se obtendra resultado que coincida con el codigo
        return resultado.size();//devolvera el numero de coincidencias
    }
    
    
    private void mostrarDatos(ObjectContainer base) {
    jTable1.setEnabled(true);
        //Guardamos en "resultado" todos proveedores de la base de Datos
        ObjectSet<Vehiculo> resultado = base.get(Vehiculo.class);
        //Creo una matriz
        String matriz[][] = new String[resultado.size()][5];
        int i = 0;
        for (Vehiculo miVehi : resultado) {//iteramos en cada elemento de "resultado"
            matriz[i][0] = miVehi.getPlaca_Vehiculo();
            matriz[i][1] = miVehi.getModelo_Vehiculo();
            matriz[i][2] = miVehi.getMarca_Vehiculo();
            matriz[i][3] = miVehi.getColor_Vehiculo();
            matriz[i][4] = miVehi.getAnioFabricacion_Vehiculo();
            i++;
        }

        // datos configurados
        jTable1.setModel(new javax.swing.table.DefaultTableModel(matriz, new String[]{"Placa", "Modelo ","Marca", "Color", "Año Fabricacion"}));
        jTable1.setEnabled(false);
    }
    
